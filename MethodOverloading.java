public class MethodOverloading {
    void add()
    {
        System.out.println("Without parameter");
    }

    void add(int a)
    {
        System.out.println("With parameter");
    }

    void add(int a,int b)
    {
        System.out.println("With parameter");
    }

    void add(double a,double b)
    {
        System.out.println("With parameter");
    }

    void add(int a, int b,int c)
    {
        System.out.println("With parameter");
    }
}
