public class literals 
{
    public static void main( String [] args)
    {
        int x=0b1111;
        int y=0xEF;
        int a=1_00_00_000;
        double z=12e5;

        System.out.print("x=");
        System.out.println(x);
        System.out.print("y=");
        System.out.println(y);
        System.out.print("z=");
        System.out.println(z);
        System.out.print("a=");
        System.out.println(a);
    }
    
}
